namespace SA3D.SA2Event
{
	/// <summary>
	/// Event language keys. Enum value is the character identifier of the language file.
	/// </summary>
	public enum EventLanguage : byte
	{
		/// <summary>
		/// English.
		/// </summary>
		English = (byte)'1',

		/// <summary>
		/// French.
		/// </summary>
		French = (byte)'2',

		/// <summary>
		/// Spanish.
		/// </summary>
		Spanish = (byte)'3',

		/// <summary>
		/// German.
		/// </summary>
		German = (byte)'4',

		/// <summary>
		/// Italian.
		/// </summary>
		Italian = (byte)'5',

		/// <summary>
		/// Japanese.
		/// </summary>
		Japanese = (byte)'J'
	}
}
